package com.uni.model;

public class StudentCourse {

	private int id;
	private int crs_id;
	private int std_id;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getCrs_id() {
		return crs_id;
	}
	public void setCrs_id(int crs_id) {
		this.crs_id = crs_id;
	}
	public int getStd_id() {
		return std_id;
	}
	public void setStd_id(int std_id) {
		this.std_id = std_id;
	}
}
